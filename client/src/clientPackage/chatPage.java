    private void sendFileButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendFileButtonMouseClicked
        // TODO add your handling code here:
        JFileChooser ch = new JFileChooser();
        int c = ch.showOpenDialog(this);
        if (c == JFileChooser.APPROVE_OPTION) {
            File f = ch.getSelectedFile();
            FileInputStream in = null;
            try {
                in = new FileInputStream(f);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            byte b[] = null;
            try {
                b = new byte[in.available()];
                in.read(b);
            } catch (IOException ex) {
                Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            messageData data = new messageData("fileSend", userName.getText(), ip.getText(), b);
            data.setStatus(typeChooser.getSelectedItem() + "");
            data.setName(captionText.getText().trim());
            try {
                objectOutputStream = new ObjectOutputStream(socket.getOutputStream());
            } catch (IOException ex) {
                Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                objectOutputStream.writeObject(data);
                objectOutputStream.flush();
            } catch (IOException ex) {
                Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            System.out.println(captionText.getText() + " Sent");
            captionText.setText("");
    }//GEN-LAST:event_sendFileButtonMouseClicked
    }
