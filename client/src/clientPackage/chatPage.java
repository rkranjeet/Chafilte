    private void sendChatButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendChatButtonMouseClicked
        // TODO add your handling code here:
        if (chatText.getText() != "") {
            msgobj = new messageData("chatSend", userName.getText(), ip.getText(), chatText.getText());
            try {
                objectOutputStream = new ObjectOutputStream(socket.getOutputStream());
            } catch (IOException ex) {
                Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                objectOutputStream.writeObject(msgobj);
            } catch (IOException ex) {
                Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    objectOutputStream.flush();
                } catch (IOException ex) {
                    Logger.getLogger(chatPage.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            chatText.setText("");
        }
    }//GEN-LAST:event_sendChatButtonMouseClicked

